UPDATE "chart_template_accounts"
SET "name" = CASE "account_no"
    WHEN '1000' THEN 'Активы'
    WHEN '1100' THEN 'Денежные средства и эквиваленты'
    WHEN '1200' THEN 'Операционные активы'
    WHEN '1300' THEN 'Внутригрупповые расчеты'
    WHEN '2000' THEN 'Обязательства'
    WHEN '2100' THEN 'Операционные обязательства'
    WHEN '3000' THEN 'Капитал'
    WHEN '4000' THEN 'Доходы'
    WHEN '5000' THEN 'Расходы'
    WHEN '1110' THEN 'Банк'
    WHEN '1210' THEN 'Резерв по ордерам'
    WHEN '1220' THEN 'Транзит'
    WHEN '1310' THEN 'Внутригрупповой неттинг'
    WHEN '2110' THEN 'Кошелек клиента'
    WHEN '2120' THEN 'Клиринг комиссий'
    WHEN '2130' THEN 'Обязательство по выплате'
    WHEN '4110' THEN 'Доход от комиссий'
    WHEN '4120' THEN 'Доход от спреда'
    WHEN '4130' THEN 'Доход от корректировок'
    WHEN '5110' THEN 'Расход по корректировкам'
    ELSE "name"
END
WHERE "account_no" IN (
    '1000', '1100', '1200', '1300',
    '2000', '2100', '3000', '4000', '5000',
    '1110', '1210', '1220', '1310',
    '2110', '2120', '2130',
    '4110', '4120', '4130', '5110'
